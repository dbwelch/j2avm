<richcontent TYPE="NOTE"><html>
  <head>
    
  </head>
  <body>
    <p>
      J2AVM is a translator from JVM bytecode to AVM2 bytecode. It allows compiled Java/JVM code to be executed on the Flash platform - in the Flash player or on the Adobe AIR runtime.
    </p>
    <p>
      The goals for the project comprise three stages:
    </p>
    <ol>
      <li>
        Allow Flash applications to be written in Java
      </li>
      <li>
        Allow Java logic to be shared between Java and Flash applications
      </li>
      <li>
        Allow Java applications to run in the Flash player
      </li>
    </ol>
    <p>
      The first goal does not include providing support for the Java libraries beyond the basics required for the Java language. It provides Java proxies for all the Flash player runtime libraries - so that Java can replace ActionScript in developing Flash applications.
    </p>
    <p>
      The second goal includes implementation of enough Java libraries to allow Java code to be written with the intention of being used in both JVM and Flash based applications. Such code will have to written with the constraints of each platform in mind.
    </p>
    <p>
      The third goal is to provide more comprehensive implementation of Java libraries, so that legacy Java code can be run, unaltered, in the Flash player.
    </p>
  </body>
